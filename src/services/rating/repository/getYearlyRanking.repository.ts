function getYearSeed(year: number): number {
  if (year === 2024) {
    return 2;
  }
  if (year === 2025) {
    return 4;
  }
  return 6;
}

export async function getYearlyRankingRepository(year: number): Promise<YearlyRankingRecord[]> {
  const normalizedYear = year === 2024 || year === 2025 || year === 2026 ? year : 2026;
  const seed = getYearSeed(normalizedYear);

  return Array.from({ length: 30 }, (_, index) => {
    const rankBoost = 30 - index;
    const wins = 8 + ((index * 3 + seed) % 26);
    const losses = 3 + ((index + seed) % 13);
    const tournaments = 4 + ((index * 2 + seed) % 11);
    const rating = Number((2.5 + rankBoost * 0.11 + seed * 0.03 + (index % 4) * 0.04).toFixed(2));

    return {
      id: `rating-${normalizedYear}-${index + 1}`,
      name: PLAYER_NAMES[index],
      wins,
      losses,
      tournaments,
      rating,
    };
  });
}
